function Delete(_url, tableId) {
    var dataTable = $('#myTable');

    swal({
        title: "Are you sure?",
        text: "Once deleted, it could not be restored!",
        icon: "warning",
        buttons: true,
        dangerMode: true
    }).then((willDelete) => {
        if (willDelete) {
            $.ajax({
                type: "DELETE",
                url: _url,
                success: function (data) {
                    if (data.success) {
                        toastr.success(data.message);
                        dataTable.ajax.reload();
                    }
                    else {
                        toastr.error(data.message);
                    }
                },

            });
        }
    }
    )
}
